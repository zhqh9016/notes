package com.taikang.dic.ltci.common.enumeration;

public enum InHospStateEnum {
  In(1, "出院"),
  out(0, "在院"),
  NULL(2, "入住编号不存在");
  private int value;
  private String desc;

  private InHospStateEnum(int value, String desc) {
    this.value = value;
    this.desc = desc;
  }

  public int getValue() {
    return value;
  }

  public void setValue(int value) {
    this.value = value;
  }

  public String getDesc() {
    return desc;
  }

  public void setDesc(String desc) {
    this.desc = desc;
  }
}
