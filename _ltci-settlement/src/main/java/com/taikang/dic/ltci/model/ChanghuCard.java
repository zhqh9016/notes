package com.taikang.dic.ltci.model;

/**
 * 个人预结算/结算接口卡信息入参
 *
 * @author itw_zhangqh
 */
public class ChanghuCard {

  private String PSAMCardNum;
  private String sCardNo;
  private String sCardType;
  private String sIDNo;
  private String sName;

  public String getPSAMCardNum() {
    return PSAMCardNum;
  }

  public void setPSAMCardNum(String pSAMCardNum) {
    PSAMCardNum = pSAMCardNum;
  }

  public String getsCardNo() {
    return sCardNo;
  }

  public void setsCardNo(String sCardNo) {
    this.sCardNo = sCardNo;
  }

  public String getsCardType() {
    return sCardType;
  }

  public void setsCardType(String sCardType) {
    this.sCardType = sCardType;
  }

  public String getsIDNo() {
    return sIDNo;
  }

  public void setsIDNo(String sIDNo) {
    this.sIDNo = sIDNo;
  }

  public String getsName() {
    return sName;
  }

  public void setsName(String sName) {
    this.sName = sName;
  }
}
