package com.taikang.dic.ltci.common.enumeration;

/**
 * 机构/人员枚举
 *
 * @author itw_zhangqh
 */
public enum ObjectTypeEnum {
  STAFF(2, "人员"),
  ORG(1, "机构");

  ObjectTypeEnum(int value, String desc) {
    this.value = value;
    this.desc = desc;
  }

  private int value;

  public int getValue() {
    return value;
  }

  public void setValue(int value) {
    this.value = value;
  }

  public String getDesc() {
    return desc;
  }

  public void setDesc(String desc) {
    this.desc = desc;
  }

  private String desc;
}
