package com.taikang.dic.ltci.portal.common.enumeration;

/**
 * 是否重评枚举类
 *
 * @author itw_xuxf
 * @version 2017年4月14日上午11:04:09
 */
public enum IsReevaluateEnum {
  NO_REEVALUATE("0", "否"),
  REEVALUATE("1", "是");
  private String value;

  private String desc;

  private IsReevaluateEnum(String value, String desc) {
    this.value = value;
    this.desc = desc;
  }

  public String getValue() {
    return value;
  }

  public String getDesc() {
    return desc;
  }

  public boolean isRest() {
    return false;
  }
}
