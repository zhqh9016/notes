package com.taikang.dic.ltci.system.common.enumeration;

/**
 * 是否有效枚举
 *
 * @author itw_liyh01
 */
public enum PushUserActionTypeEnum {
  CREATE("1", "新增"),
  UPDATE("2", "更新"),
  DELETE("3", "删除");
  private String value;

  private String desc;

  private PushUserActionTypeEnum(String value, String desc) {
    this.value = value;
    this.desc = desc;
  }

  public String getValue() {
    return value;
  }

  public String getDesc() {
    return desc;
  }

  public boolean isRest() {
    return false;
  }
}
