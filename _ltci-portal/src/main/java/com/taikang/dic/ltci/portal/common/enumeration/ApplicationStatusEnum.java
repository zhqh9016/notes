package com.taikang.dic.ltci.portal.common.enumeration;

/**
 * 状态码枚举类
 *
 * @author itw_liuxy
 * @description
 * @createTime 2017年4月6日 上午10:41:44
 */
public enum ApplicationStatusEnum {
  NO_ACCEPT("00", "未受理"),
  ALREADY_ACCEPT("01", "已受理"),
  SUSPEND_ACCEPT("02", "暂停受理"),
  PASS_ACCEPT("03", "审核通过"),
  NOT_PASS_ACCEPT("04", "审核不通过");
  private String value;

  private String desc;

  private ApplicationStatusEnum(String value, String desc) {
    this.value = value;
    this.desc = desc;
  }

  public String getValue() {
    return value;
  }

  public String getDesc() {
    return desc;
  }
}
