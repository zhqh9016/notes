package com.taikang.dic.ltci.portal.common.enumeration;

/**
 * 是否是菜单枚举
 *
 * @author itw_liyh01
 */
public enum IsMenuEnum {
  IS_MENU_YES(1, "是"),
  IS_MENU_NO(0, "否");
  private int value;

  private String desc;

  private IsMenuEnum(int value, String desc) {
    this.value = value;
    this.desc = desc;
  }

  public int getValue() {
    return value;
  }

  public String getDesc() {
    return desc;
  }

  public boolean isRest() {
    return false;
  }
}
