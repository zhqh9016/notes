package com.taikang.dic.ltci.common.enumeration;

/**
 * 给移动端的状态
 *
 * @author itw_caoxy
 */
public enum TaskStatusEnum {
  COMPLETE("2", "已完成"),
  NOT_COMPLETE("1", "未完成");
  private String value;

  private String desc;

  private TaskStatusEnum(String value, String desc) {
    this.value = value;
    this.desc = desc;
  }

  public String getValue() {
    return value;
  }

  public String getDesc() {
    return desc;
  }

  public boolean isRest() {
    return false;
  }
}
