package com.taikang.dic.ltci.mqmodel;

import java.util.Date;
import java.util.List;

public class TaskResultMqModel {

  /** 巡查人ID */
  private String staffCode;

  /** 巡查人姓名 */
  private String staffName;

  /** 巡查结果 */
  private String patrolResult;

  /** 巡查时间 */
  private Date patrolTime;

  /** 巡查任务编号 */
  private String patrolTaskId;

  /** 巡查资料列表 */
  private List<PatrolDatumModel> patrolInfo;

  public String getStaffCode() {
    return staffCode;
  }

  public void setStaffCode(String staffCode) {
    this.staffCode = staffCode;
  }

  public String getStaffName() {
    return staffName;
  }

  public void setStaffName(String staffName) {
    this.staffName = staffName;
  }

  public String getPatrolResult() {
    return patrolResult;
  }

  public void setPatrolResult(String patrolResult) {
    this.patrolResult = patrolResult;
  }

  public Date getPatrolTime() {
    return patrolTime;
  }

  public void setPatrolTime(Date patrolTime) {
    this.patrolTime = patrolTime;
  }

  public String getPatrolTaskId() {
    return patrolTaskId;
  }

  public void setPatrolTaskId(String patrolTaskId) {
    this.patrolTaskId = patrolTaskId;
  }

  public List<PatrolDatumModel> getPatrolInfo() {
    return patrolInfo;
  }

  public void setPatrolInfo(List<PatrolDatumModel> patrolInfo) {
    this.patrolInfo = patrolInfo;
  }
}
