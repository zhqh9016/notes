package com.taikang.dic.ltci.common.enumeration;

/**
 * 是否享受待遇枚举
 *
 * @author itw_liuxy
 */
public enum NurseTreatmentEnum {
  NO("0", "不能享受"),
  YES("1", "可以享受");
  private String value;

  private String desc;

  private NurseTreatmentEnum(String value, String desc) {
    this.value = value;
    this.desc = desc;
  }

  public String getValue() {
    return value;
  }

  public String getDesc() {
    return desc;
  }

  public boolean isRest() {
    return false;
  }
}
