package com.taikang.dic.ltci.common.enumeration;

public enum InHospStateEnum {
  OUT("1", "出院"),
  IN("0", "在院"),
  NULL("2", "无");
  private String value;

  private String desc;

  private InHospStateEnum(String value, String desc) {
    this.value = value;
    this.desc = desc;
  }

  public String getValue() {
    return value;
  }

  public String getDesc() {
    return desc;
  }

  public boolean isRest() {
    return false;
  }
}
