public class ThrowsException extends RuntimeException {

  private static final long serialVersionUID = 1L;

  public ThrowsException(String message) {
    super(message);
  }

  public ThrowsException(String object, String id) {
    super(object + "[" + id + "]不存在。");
  }
}
